def div_usual(number1: float, number2: float):
    return number1 / number2


def div_integer(number1, number2):
    return number1 // number2


def div_modul(number1, number2):
    return number1 % number2


def mult(number_1, number_2):
    return number_1 * number_2


def pow_usual(number_1, number_2):
    return number_1 ** number_2


def sub_value(number_1, number_2):
    return number_1 - number_2


def sum_value(number_1, number_2):
    return number_1 + number_2
